    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        try {
            String patente = jTextFieldPatente.getText();
            String modelo = jTextFieldVehiculo.getText();
            String tipo = jTextFieldTipo.getText().toUpperCase();
            int anio = Integer.parseInt(jTextFieldAnio.getText());
            double capCarga = Double.parseDouble(jTextFieldCapCarga.getText());
            String nombreMarca = jTextFieldNombreMarca.getText();

            Marca marcaGenerica = new Marca(nombreMarca, "N/A");
            Responsable resp = new Responsable(jTextFieldSucursalResponsable.getText(), "0", "0");
            Sucursal suc = new Sucursal(
                jTextFieldSucursalCodigo.getText(),
                jTextFieldSucursalDireccion.getText(),
                jTextFieldSucursalCiudad.getText(),
                resp
            );

            Vehiculo nuevoVehiculo;

            if (tipo.equals("ELECTRICO")) {
                double kwh = Double.parseDouble(jTextFieldkmLt.getText());
                nuevoVehiculo = new VehiculoElectrico(patente, marcaGenerica, modelo, anio, capCarga, suc, kwh);
            } else {
                double kmLt = Double.parseDouble(jTextFieldkmLt.getText());
                double litrosEx = Double.parseDouble(jTextFieldLitrosExtra.getText());
                nuevoVehiculo = new VehiculoCombustible(patente, marcaGenerica, modelo, anio, capCarga, suc, kmLt, litrosEx);
            }
            Controlador.agregarVehiculo(nuevoVehiculo);

            javax.swing.JOptionPane.showMessageDialog(this, "Vehículo agregado con éxito");
            this.dispose();

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Verifique que los campos numéricos sean correctos.");
        }
    }//GEN-LAST:event_jButtonAgregarActionPerformed

